-- Admin sponsors: catálogo, inventario de slots, placements y tracking básico.
-- Lectura pública solo vía vista curada; las tablas crudas quedan admin-only.

create table if not exists public.sponsors (
  id uuid primary key default gen_random_uuid(),
  slug text not null unique,
  name text not null,
  status text not null default 'active' check (status in ('active', 'paused', 'archived')),
  website_url text,
  logo_url text,
  brand_color text,
  contact_name text,
  contact_email text,
  billing_email text,
  contract_starts_on date,
  contract_ends_on date,
  notes text,
  created_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint sponsors_slug_format_chk check (slug ~ '^[a-z0-9]+(?:-[a-z0-9]+)*$'),
  constraint sponsors_brand_color_chk check (brand_color is null or brand_color ~ '^#[0-9A-Fa-f]{6}$'),
  constraint sponsors_contract_dates_chk check (
    contract_starts_on is null
    or contract_ends_on is null
    or contract_ends_on >= contract_starts_on
  )
);

create table if not exists public.sponsor_slots (
  id uuid primary key default gen_random_uuid(),
  key text not null unique,
  surface text not null,
  label text not null,
  description text,
  max_active_placements int not null default 1 check (max_active_placements > 0),
  base_price_cents int not null default 0 check (base_price_cents >= 0),
  currency text not null default 'USD',
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint sponsor_slots_key_format_chk check (key ~ '^[a-z0-9]+(?:_[a-z0-9]+)*$')
);

create table if not exists public.sponsor_placements (
  id uuid primary key default gen_random_uuid(),
  sponsor_id uuid not null references public.sponsors(id) on delete cascade,
  slot_id uuid not null references public.sponsor_slots(id) on delete restrict,
  status text not null default 'draft' check (status in ('draft', 'active', 'paused', 'archived')),
  headline text not null,
  body text,
  image_url text,
  image_alt text,
  target_url text,
  priority int not null default 0,
  starts_at timestamptz not null default now(),
  ends_at timestamptz,
  contract_amount_cents int not null default 0 check (contract_amount_cents >= 0),
  currency text not null default 'USD',
  created_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint sponsor_placements_dates_chk check (ends_at is null or ends_at > starts_at)
);

create table if not exists public.sponsor_placement_events (
  id bigint generated by default as identity primary key,
  placement_id uuid not null references public.sponsor_placements(id) on delete cascade,
  event_type text not null check (event_type in ('impression', 'click')),
  user_id uuid references public.profiles(id) on delete set null,
  session_id text,
  pathname text,
  referrer text,
  metadata jsonb not null default '{}'::jsonb,
  occurred_at timestamptz not null default now()
);

create index if not exists sponsor_placements_slot_status_idx
  on public.sponsor_placements(slot_id, status, starts_at, ends_at);
create index if not exists sponsor_placements_sponsor_idx
  on public.sponsor_placements(sponsor_id);
create index if not exists sponsor_placement_events_placement_type_idx
  on public.sponsor_placement_events(placement_id, event_type, occurred_at desc);
create index if not exists sponsor_placement_events_occurred_idx
  on public.sponsor_placement_events(occurred_at desc);

