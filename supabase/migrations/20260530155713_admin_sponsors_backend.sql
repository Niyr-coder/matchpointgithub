-- Las inserciones de tracking pasan por server action + service role. No hay
-- policy de INSERT pública para evitar abuso directo sobre la tabla de eventos.

create or replace view public.active_sponsor_placements
with (security_barrier = true) as
select
  p.id as placement_id,
  sl.key as slot_key,
  sl.surface,
  sl.label as slot_label,
  p.priority,
  p.headline,
  p.body,
  p.image_url,
  p.image_alt,
  p.target_url,
  p.starts_at,
  p.ends_at,
  s.id as sponsor_id,
  s.name as sponsor_name,
  s.slug as sponsor_slug,
  s.logo_url as sponsor_logo_url,
  s.website_url as sponsor_website_url,
  s.brand_color as sponsor_brand_color
from public.sponsor_placements p
join public.sponsors s on s.id = p.sponsor_id
join public.sponsor_slots sl on sl.id = p.slot_id
where p.status = 'active'
  and s.status = 'active'
  and sl.is_active
  and p.starts_at <= now()
  and (p.ends_at is null or p.ends_at > now());

grant select on public.active_sponsor_placements to anon, authenticated;

