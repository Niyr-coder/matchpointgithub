create or replace function public.help_record_article_view(p_article_id uuid)
returns int
language plpgsql
security definer
set search_path = public
as $$
declare
  _views int;
begin
  if auth.uid() is null then
    raise exception 'AUTH.UNAUTHENTICATED' using errcode = '28000';
  end if;

  update public.help_articles
  set view_count = view_count + 1
  where id = p_article_id
    and status = 'published'
  returning view_count into _views;

  if _views is null then
    raise exception 'HELP.ARTICLE_NOT_FOUND' using errcode = '22023';
  end if;

  return _views;
end;
$$;

revoke all on function public.help_record_article_view(uuid) from public;
grant execute on function public.help_record_article_view(uuid) to authenticated;

